- (UIImage *)imagescaleToSize:(CGSize)size
{
    CGFloat scale = 1;
    
    NSData *uploadImageData = UIImagePNGRepresentation(self);
    CGSize originalsize = self.size;
    CGFloat newW = originalsize.width;
    CGFloat newH = originalsize.height;
    
    CGFloat oldW = size.width;
    CGFloat oldH = size.height;
    
    if (oldW > oldH && oldW > newW ) {//如果宽度大的话根据宽度固定大小缩放
        scale = (CGFloat) (oldW / newW);
    } else if (oldW < oldH && oldH > newH) {
        scale = (CGFloat) (oldH / newH);
    }
    
    [self drawInRect:CGRectMake(0, 0, size.width, size.height)];
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    uploadImageData = UIImageJPEGRepresentation(image, scale);
    
    return [[UIImage alloc] initWithData:uploadImageData];
}

